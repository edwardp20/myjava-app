public class Girlfriend {
    private String name;
    private int age;

    // 空参构造
    public Girlfriend() {
    }

    // 全参构造
    public Girlfriend(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Getter 和 Setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}