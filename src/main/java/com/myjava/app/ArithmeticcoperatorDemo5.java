package com.myjava.app;


public class ArithmeticcoperatorDemo5{
    public static void main(String[] args){
        int c=10;
        c++;
        System.out.println(c);//11
        ++c;
        System.out.println(c);//12
        c--;
        System.out.println(c);//11
        --c;
        System.out.println(c);//10
        
        
        
        
        
        
        
        
        
     
    }
}
